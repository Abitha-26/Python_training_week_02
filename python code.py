
class ClassName:
    def __init__(self, name1, age2):
        self.name = name1
        self.age = age2

    def method_name(self):
        print(f"name: {self.name}, age: {self.age}")

# Creating an instance of ClassName
obj = ClassName("Abitha", 19)

# Calling the method
obj.method_name()
